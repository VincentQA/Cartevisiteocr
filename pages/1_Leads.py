import streamlit as st
import sqlite3
import pandas as pd

st.set_page_config(page_title="Le charte visite üê± - Voir les leads", layout="centered")
st.title("Le charte visite üê± - Voir les leads")

@st.cache_resource
def get_db_connection():
    # On active check_same_thread=False pour √©viter des probl√®mes de threads
    return sqlite3.connect("leads.db", check_same_thread=False)

conn = get_db_connection()
cursor = conn.cursor()

# Cr√©ation de la table si elle n'existe pas
try:
    cursor.execute("""
    CREATE TABLE IF NOT EXISTS leads (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        ocr_text TEXT,
        nom TEXT,
        prenom TEXT,
        telephone TEXT,
        mail TEXT,
        agent1 TEXT,
        agent2 TEXT,
        agent3 TEXT,
        qualification TEXT,
        note TEXT,
        timestamp DATETIME DEFAULT CURRENT_TIMESTAMP
    )
    """)
    conn.commit()
except Exception as e:
    st.error("Erreur lors de la cr√©ation de la table : " + str(e))

# R√©cup√©ration des donn√©es
try:
    cursor.execute("SELECT id, ocr_text, nom, prenom, telephone, mail, agent1, agent2, agent3, qualification, note, timestamp FROM leads ORDER BY timestamp DESC")
    rows = cursor.fetchall()
    
    if rows:
        columns = [desc[0] for desc in cursor.description]
        df = pd.DataFrame(rows, columns=columns)
        st.dataframe(df)
    else:
        st.info("Aucun lead n'a √©t√© enregistr√© pour le moment.")
except Exception as e:
    st.error("Erreur lors de la r√©cup√©ration des leads : " + str(e))

conn.close()
